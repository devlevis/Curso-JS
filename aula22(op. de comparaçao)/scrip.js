/*
OPERADORES DE COMPARAÇÃO:
> - maior que
>= - maior ou igual que
< - menor que
<= - menor ou igual que
=== - igualdade restrita (valor e tipo)
!== - diferente estrito (valor e tipo)
IRÃO RESPOSTAS EM BOLLEAN
*/
const num1 = 12;
const num2 = '23';
console.log(num1 !== num2); 
console.log(`12 é menor que 23?:${num1 < num2}`)