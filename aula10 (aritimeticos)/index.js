/*
Aritiméticos:
+ = adição / concatenação (juntar os números)
- = subtração
* = multiplicação
** = pontenciação
% = resto da divisão
INCREMENTO = --
DECREMENTO = ++  
*/

const num1 = 10;
const num2 = 4;

console.log(num1 % num2)

//INCREMENTO:

let contador = 1;
contador++; //2
contador++; //3
contador++; //4
contador++; //5
contador++; //6
console.log(contador);

//DECREMENTO:

let tirador = 10
console.log(--tirador); //9
console.log(--tirador) //8 ...

let contador2 = 2;
contador2 *= 5; //MULTIPLIQUEI O contador2 POR 5
console.log(contador2)

